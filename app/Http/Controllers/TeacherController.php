     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $user = new User([
            'name'=>$request->name,
            'email'=>$request->email,
            'password'=>$request->password,
            'role_id'=>$request->role_id,
        ]);
        $user->save();
        $teacher = new Teacher([
            'phone'=>$request->phone,
            'user_id'=>$user->id,
        ]);
        $teacher->save();
    }

